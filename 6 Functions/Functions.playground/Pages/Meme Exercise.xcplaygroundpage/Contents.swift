func chorus() {
    print("Yeo yeo yeo")
    print("My name is Joe")
}
func verseOne() {
    print("I just came from Mexico")
}
func first() {
    chorus()
    verseOne()
}

func space() {
    print(".......^^.......")
}

func full() {
    space()
    first()
    space()
}

full()














